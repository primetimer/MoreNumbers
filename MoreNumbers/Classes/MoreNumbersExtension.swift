import Foundation

public extension Double {
    static var phi: Double { return Double(sqrt(5.0) + 1.0 ) / 2.0 }
    static var psi: Double { return Double(sqrt(5.0) - 1.0 ) / 2.0 }
    static var gamma : Double { return 0.577215664901532860606512090 }
    static var mill : Double { return 1.3063778838630806904686144926026057129167845851 }
}


